def run():
    c = int(input())
    for _ in range(c):
        n = int(input())
        l_l = [[int(j) for j in input().split()] for _ in range(n)]
        check = l_l[0][2]
        res = [l_l[0][0], l_l[0][1]]
        l_l.remove(l_l[0])
        while n != len(res):
            next = list(filter(lambda x: x[0] == res[-1], l_l))[0]
            l_l.remove(next)
            if next[1] == res[res.index(next[0]) - 1]:
                res.append(next[2])
            else:
                res.append(next[1])
        else:
            if check != res[-1]:
                print('error')
        for i in range(n//2):
            print(f'{res[i]} {res[i+n//2]}')
        print('')



