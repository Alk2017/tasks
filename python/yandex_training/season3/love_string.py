import math


def run():
    line = list(input())
    size = len(line)
    k = math.ceil(size / 2)
    res = dict()
    for ch in line:
        if ch not in res.keys():
            res[ch] = 0
    sum = 0
    j = 0
    if size%2== 0:
        for i in range(0, k):
            sum += (k - j) * 2
            j += 1
            res[line[i]] += sum
            res[line[-(1 + i)]] += sum
    else:
        for i in range(0, k):
            sum += (k - j) * 2
            j += 1
            res[line[i]] += sum
            if i != k-1:
                res[line[-(1 + i)]] += sum
            res[line[i]] -= j
            if i != k - 1:
                res[line[-(1 + i)]] -= j

    keys = list(res.keys())
    keys.sort()
    for k in keys:
        print(f'{k}: {res[k]}')


if __name__ == '__main__':
    run()
